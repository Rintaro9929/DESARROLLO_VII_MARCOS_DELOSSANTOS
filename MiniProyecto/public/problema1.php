|--------------------------------------------------------------------------
| Inicializar suma
|--------------------------------------------------------------------------
*/

$suma = 0;

/*
|--------------------------------------------------------------------------
| Ciclo for
|--------------------------------------------------------------------------
|
| Recorre desde 1 hasta 1000
| y acumula la suma.
|
*/

